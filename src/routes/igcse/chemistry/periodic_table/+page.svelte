<section>
  <h1>Periodic Table</h1>
  <h2>The Periodic Table</h2>
  <p>
    The periodic table is used as a method of classifying and predicting the
    properties of elements.
  </p>
  <h2>Periodic Trends</h2>
  <p>
    Elements become more non-metallic across a period. Elements near the bottom
    left corner of the table are the most metallic while elements near the top
    right corner are the least metallic.
  </p>
  <p>
    An element's group number is equal to the number of electrons in its outer
    shell (valence electrons). For example, oxygen is in group 6 and has 6
    valence electrons.
  </p>
  <h2>Group Properties</h2>
  <h3>Group I</h3>
  <p>
    This group, known as the alkali earth metals, is a series of soft metals
    that react vigorously with cold water to produce an alkali and hydrogen gas.
  </p>
  <p>Going down the group:</p>
  <ul>
    <li>reactivity increases</li>
    <li>density increases</li>
    <li>melting point decreases</li>
  </ul>
  Examples: lithium, sodium, and potassium
  <h3>Group VII</h3>
  <p>
    Group VII, otherwise known as Group 17, are the halogens. These are a series
    of non-metals that form diatomic molecules.
  </p>
  <p>Going down the group:</p>
  <ul>
    <li>reactivity decreases</li>
    <li>density increases</li>
    <li>boiling point increases</li>
    <li>color darkens</li>
  </ul>
  Examples: fluorine, chlorine, bromine, and iodine
  <h3>Transition Elements</h3>
  <p>The transition elements are metals that:</p>
  <ul>
    <li>have high melting points</li>
    <li>have high densities</li>
    <li>form colored compounds</li>
    <li>show catalytic activity</li>
    <li>have variable oxidation numbers</li>
  </ul>
  Examples: iron, cobalt, nickel, and copper
  <h3>Group VIII</h3>
  <p>
    Group VIII, otherwise known as Group 18, are the noble gases. These are a
    series of monatomic gases which are chemically inert (unreactive). These
    gases are unreactive because their valence (outer) shell is full. This means
    that they rarely form compounds.
  </p>
  <Callout emoji="ðŸ¤”">Group VIII is also sometimes called Group 0.</Callout>
</section>
