  <h3>Recycling</h3>
  <p>It is important to recycle metals because metal ores are a finite resource.</p>
  <h2>Uses of Metals</h2>
  <h3>Aluminium</h3>
  <p>Aluminium is used in:</p>
  <ul>
    <li>aircraft parts because of its strength and low density</li>
    <li>food containers because of its resistance to corrosion</li>
  </ul>
  <p>
    Aluminium has an apparent unreactivity and resistance to corrision because of a protective oxide
    layer that adheres to the surface of aluminium. This layer prevents further oxidation.
  </p>
  <h3>Steel</h3>
  <p>
    Steel is an alloy made from iron and carbon. Mild steel is used in car bodies and machinery
    while stainless steel (an alloy of steel with chromium and nickel) is used in cutlery. Steel is
    an alloy that is stronger and has a higher melting point than its constituents.
  </p>
  <h3>Zinc</h3>
  <p>Zinc is used for:</p>
  <ul>
    <li>galvanising iron and steel to prevent corrosion (rusting)</li>
    <li>making brass by mixing it with copper</li>
  </ul>
  Another alloy of copper is bronze, which is made from copper and tin.
